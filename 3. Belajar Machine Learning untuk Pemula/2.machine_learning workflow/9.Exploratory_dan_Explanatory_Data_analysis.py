"""
1. Exploratory Data Analysis (EDA)
EDA adalah proses eksplorasi awal terhadap data sebelum analisis lebih lanjut dilakukan. Tujuan utamanya adalah untuk memahami pola, karakteristik, dan hubungan antar variabel dalam data tanpa hipotesis yang telah ditentukan sebelumnya.

Metode yang digunakan dalam EDA:

- Statistik Deskriptif: Menggunakan mean, median, modus, standar deviasi, dan distribusi frekuensi untuk memahami karakteristik dasar data.

- Visualisasi Data:
    - Histogram → untuk melihat distribusi data.
    - Scatter plot → untuk mengidentifikasi pola hubungan antar variabel.
    - Box plot → untuk mendeteksi outlier atau anomali.
    - Heatmap → untuk melihat korelasi antar variabel.

Pembersihan Data: Mengidentifikasi dan menangani missing values serta outlier yang dapat mempengaruhi analisis.

"""

"""
2. Explanatory Data Analysis (ExDA)
ExDA dilakukan setelah EDA selesai, dan berfokus pada cara menyampaikan hasil analisis kepada audiens yang lebih luas, seperti manajer, stakeholder, atau klien.

Metode yang digunakan dalam ExDA: 

- Narasi yang terstruktur: Memulai dengan pernyataan masalah, menyajikan data dan analisisnya, lalu diakhiri dengan kesimpulan yang jelas.

- Visualisasi yang efektif:

    - Bar chart & line chart → untuk menunjukkan tren.
    - Pie chart → untuk menunjukkan proporsi.
    - Infografis → agar temuan lebih mudah dipahami.

- Storytelling: Data dikemas dalam bentuk cerita sehingga audiens dapat dengan mudah memahami hubungan antar variabel dan implikasi dari hasil analisis.

ExDA dirancang untuk memastikan bahwa hasil analisis dapat digunakan dalam pengambilan keputusan.

"""

"""
Kesimpulannya, EDA digunakan untuk menemukan pola dan memahami data, sementara ExDA berfungsi untuk menyampaikan hasil analisis dengan cara yang lebih mudah dipahami.

"""